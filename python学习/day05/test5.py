# 题1：有两个集合{1, 2, 3, 4, 5}和{4, 5, 6, 7,8}，
# 用集合运算找出它们的交集、并集、差集。
a={1, 2, 3, 4, 5}
b={4, 5, 6, 7,8}
print(a&b) # 交集
print(a|b) # 并集
print(a-b) # 差集
# 题2：给列表["苹果", "香蕉", "苹果", "橘子", "香蕉", "葡萄"]
# 去重。
fruits=["苹果", "香蕉", "苹果", "橘子", "香蕉", "葡萄"]
result=list(set(fruits))
print(result)
# 题3：用列表推导式生成1到20中所有奇数的立方。
# （提示：x % 2 == 1判断奇数）
n=[x**3 for x in range(1,21)if x%2==1]
print(n)

